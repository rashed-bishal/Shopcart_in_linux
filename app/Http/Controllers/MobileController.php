    {
        $mobileitem = new MobileItem();
        $mobileitem->model_name = $request->input('model_name');
        $mobileitem->model_number = $request->input('modei_number');
        $mobileitem->price = $request->input('price');
        $mobileitem->mobile_brand_id = 2;
        $mobileitem->save();
        return redirect()->route('admin.products.create');
    }
